def prep_concept_data(concepts: list[Concept], client: httpx.Client,
                      audio_enabled: bool = True, force: bool = False) -> dict[str, list[bytes]]:
    """
    Download / generate all images and audio for each concept.
    Validates strictly.  Saves to disk for reuse.
    Returns {concept_word: [validated_image_bytes, ...]}
    """
    all_images: dict[str, list[bytes]] = {}
    DATA_DIR.mkdir(parents=True, exist_ok=True)

    for concept in concepts:
        word = concept.word
        concept_dir = DATA_DIR / word
        concept_dir.mkdir(exist_ok=True)
        manifest_path = concept_dir / "manifest.json"

        # Load cached manifest
        if manifest_path.exists() and not force:
            with open(manifest_path) as f:
                manifest = json.load(f)
            imgs = []
            for img_name in manifest.get("images", []):
                p = concept_dir / img_name
                if p.exists():
                    imgs.append(p.read_bytes())
            if imgs:
                all_images[word] = imgs
                print(f"  {word}: cached ({len(imgs)} images)")
                continue

        images_validated: list[bytes] = []
        image_names: list[str] = []

        # 1. Synthetic (abstract concepts) -- perfect quality guaranteed
        synthetic = generate_synthetic(concept)
        if synthetic:
            ok, reason = validate_image(synthetic, word, generated=True)
            if ok:
                fname = f"{word}_synth.jpg"
                (concept_dir / fname).write_bytes(synthetic)
                images_validated.append(synthetic)
                image_names.append(fname)

        # 2. Wikipedia main image (concrete/abstract concepts)
        if len(images_validated) < MAX_IMAGES_PER and concept.type not in ("number",):
            wiki_img = download_wikipedia_image(concept.wiki_title, client)
            if wiki_img:
                ok, reason = validate_image(wiki_img, word)
                if ok:
                    fname = f"{word}_wiki_0.jpg"
                    (concept_dir / fname).write_bytes(wiki_img)
                    images_validated.append(wiki_img)
                    image_names.append(fname)
                else:
                    print(f"  [REJECT] {word} wiki_main: {reason}")
            else:
                print(f"  [WARN] {word}: wiki thumbnail not available")

        # 3. Additional Wikimedia images
        if len(images_validated) < MAX_IMAGES_PER and concept.type in ("concrete", "action"):
            extras = download_wikimedia_images(concept.wiki_title, client,
                                               max_images=MAX_IMAGES_PER - len(images_validated))
            for i, extra in enumerate(extras):
                ok, reason = validate_image(extra, word)
                if ok:
                    fname = f"{word}_wiki_{i+1}.jpg"
                    (concept_dir / fname).write_bytes(extra)
                    images_validated.append(extra)
                    image_names.append(fname)
                else:
                    print(f"  [REJECT] {word} extra_{i}: {reason}")

        # 4. Text-card fallback -- guarantees every concept has at least one image
        if not images_validated:
            try:
                card = generate_text_card_image(concept)
                ok, reason = validate_image(card, word, generated=True)
                if ok:
                    fname = f"{word}_textcard.jpg"
                    (concept_dir / fname).write_bytes(card)
                    images_validated.append(card)
                    image_names.append(fname)
                    print(f"  [CARD] {word}: generated text-card fallback")
                else:
                    print(f"  [ERR] {word} text-card failed validation: {reason}")
            except Exception as e:
                print(f"  [ERR] {word} text-card generation: {e}")

        # 5. Audio (AWS Polly)
        if audio_enabled and _HAS_BOTO3:
            audio_path = concept_dir / f"{word}_matthew.wav"
            if not audio_path.exists() or force:
                audio = generate_audio(f"{word}. {concept.definition}", voice_id="Matthew")
                if audio:
                    audio_path.write_bytes(audio)
            audio_path2 = concept_dir / f"{word}_joanna.wav"
            if not audio_path2.exists() or force:
                audio2 = generate_audio(concept.example, voice_id="Joanna")
                if audio2:
                    audio_path2.write_bytes(audio2)

        # Save manifest
        with open(manifest_path, "w") as f:
            json.dump({"word": word, "type": concept.type,
                        "images": image_names, "validated": len(images_validated)}, f)

        all_images[word] = images_validated
        icon = "[OK]" if images_validated else "[XX]"
        print(f"  {icon} {word}: {len(images_validated)} images")
        time.sleep(0.15)  # polite rate limit for Wikipedia

    return all_images

