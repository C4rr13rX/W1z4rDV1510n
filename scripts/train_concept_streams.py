def fetch_archive_videos(query: str = "prelinger",
                          collection: str = "prelinger",
                          max_items: int = 5,
                          client: Optional[httpx.Client] = None) -> list[dict]:
    """
    Search Archive.org for relevant educational/ephemeral films.
    Returns list of {identifier, title, description, download_url}.
    """
    if client is None:
        client = httpx.Client()
    try:
        r = client.get(
            "https://archive.org/advancedsearch.php",
            params={
                "q": f"collection:{collection} AND {query}",
                "fl[]": ["identifier", "title", "description"],
                "mediatype": "movies",
                "output": "json",
                "rows": max_items,
                "page": 1,
            },
            timeout=20,
        )
        docs = r.json().get("response", {}).get("docs", [])
        results = []
        for doc in docs:
            iid = doc.get("identifier", "")
            # Get download info
            meta_r = client.get(f"https://archive.org/metadata/{iid}", timeout=15)
            files = meta_r.json().get("files", [])
            video_url = None
            for f in files:
                name = f.get("name", "")
                if name.endswith(".mp4") or name.endswith(".mpeg"):
                    video_url = f"https://archive.org/download/{iid}/{name}"
                    break
            if video_url:
                results.append({
                    "identifier": iid,
                    "title": doc.get("title", iid),
                    "description": str(doc.get("description", ""))[:200],
                    "download_url": video_url,
                })
        return results
    except Exception as e:
        print(f"  [ERR] archive search: {e}")
        return []

def extract_video_frames(video_path: Path, fps: float = 1.0,
                          audio_sr: int = 16000) -> tuple[list[bytes], Optional[bytes]]:
    """
    Extract frames at fps and audio from a video using ffmpeg.
    Returns (frame_jpeg_list, audio_wav_bytes).
    """
    import subprocess, tempfile, glob
    frames_dir = video_path.parent / f"{video_path.stem}_frames"
    frames_dir.mkdir(exist_ok=True)
    # Extract frames
    cmd_frames = [
        "ffmpeg", "-i", str(video_path), "-vf", f"fps={fps}",
        "-q:v", "5", str(frames_dir / "%06d.jpg"), "-y", "-loglevel", "error"
    ]
    subprocess.run(cmd_frames, check=False)
    frame_files = sorted(glob.glob(str(frames_dir / "*.jpg")))
    frames = []
    for p in frame_files[:120]:  # cap at 2 minutes at 1fps
        data = Path(p).read_bytes()
        ok, _ = validate_image(data, "video_frame")
        if ok:
            frames.append(data)

    # Extract audio as 16kHz mono WAV
    audio_path = video_path.parent / f"{video_path.stem}_audio.wav"
    cmd_audio = [
        "ffmpeg", "-i", str(video_path),
        "-ar", str(audio_sr), "-ac", "1", "-f", "wav",
        str(audio_path), "-y", "-loglevel", "error"
    ]
    subprocess.run(cmd_audio, check=False)
    audio_wav = audio_path.read_bytes() if audio_path.exists() else None

    return frames, audio_wav

def train_video_stream(title: str, frames: list[bytes], audio_wav: Optional[bytes],
                        client: httpx.Client, chunk_size: int = 8) -> int:
    """
    Train a video as overlapping temporal sequences of frames.
    Each chunk of chunk_size frames = one train_sequence call.
    Audio chunks are paired with frame chunks if available.
    Returns number of sequences trained.
    """
    trained = 0
    step = max(1, chunk_size // 2)  # 50% overlap for temporal coherence
    for start in range(0, len(frames) - 1, step):
        chunk = frames[start:start + chunk_size]
        if len(chunk) < 2:
            break
        frame_payloads = []
        for i, img in enumerate(chunk):
            t = float(i) / 1.0  # 1fps -> 1s per frame
            frame_payloads.append({
                "t_secs": round(t, 2),
                "modality": "image",
                "data_b64": b64(img),
                "lr_scale": LR_SEQ,
            })
        if api_train_sequence(client, frame_payloads, tau=3.0):
            trained += 1
    return trained

