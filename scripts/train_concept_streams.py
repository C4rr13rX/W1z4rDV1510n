def train_meta_streams(client: httpx.Client) -> dict:
    counts = {}
    for domain, sequences in META_STREAMS.items():
        fired = 0
        for seq in sequences:
            frames = []
            t = 0.0
            for word in seq:
                # No keyboard events: meta-stream frames are domain-concept chains,
                # not typing simulations. Lean label sets keep bridge computation fast.
                frames.append({
                    "t_secs": round(t, 2),
                    "modality": "text",
                    "text": word,
                    "spans": [{
                        "text": word, "role": "body", "bold": False, "italic": False,
                        "size_ratio": 1.0, "indent": 0,
                        "x_frac": 0.5, "y_frac": 0.5,
                        "seq_index": 0, "seq_total": 1,
                    }],
                    "lr_scale": LR_SEQ,
                })
                t += 1.5
            if api_train_sequence(client, frames, tau=1.5):
                fired += 1
        counts[domain] = fired
        print(f"  {domain}: {fired}/{len(sequences)} sequences")
    return counts

