def download_wikimedia_images(wiki_title: str, client: httpx.Client,
                               max_images: int = 3) -> list[bytes]:
    """Fetch up to max_images additional images from the Wikipedia media list."""
    title = wiki_title.replace(" ", "_")
    images: list[bytes] = []
    try:
        r = client.get(
            f"https://en.wikipedia.org/api/rest_v1/page/media-list/{title}",
            timeout=15, follow_redirects=True,
            headers={"User-Agent": "W1z4rDV1510n/1.0 (c4rr13rX@gmail.com)"},
        )
        if r.status_code != 200:
            return images
        items = r.json().get("items", [])
        for item in items:
            if len(images) >= max_images:
                break
            if item.get("type") != "image":
                continue
            srcset = item.get("srcset", [])
            url = None
            # Prefer the highest-res variant up to 640px wide
            for src in reversed(srcset):
                if int(src.get("width", 0)) <= 640:
                    url = src.get("src")
                    break
            if not url:
                continue
            if not url.startswith("http"):
                url = "https:" + url
            try:
                ir = client.get(url, timeout=20, follow_redirects=True,
                                headers={"User-Agent": "W1z4rDV1510n/1.0 (c4rr13rX@gmail.com)"})
                if ir.status_code == 200:
                    images.append(ir.content)
            except Exception:
                continue
    except Exception:
        pass
    return images

# ---------------------------------------------------------------------------
# Image quality validation -- STRICT gates
# ---------------------------------------------------------------------------

def validate_image(data: bytes, concept_word: str, generated: bool = False) -> tuple[bool, str]:
    """
    Return (ok, reason).  Every piece of image data must pass before training.
    Generated images skip the variance check (we control their content exactly).
    """
    # Generated images are correct by construction -- skip the byte-size gate
    # (a synthetic number on a plain background compresses to ~6 KB legitimately)
    if not generated and len(data) < MIN_FILE_BYTES:
        return False, f"too small ({len(data)} bytes < {MIN_FILE_BYTES})"
    try:
        img = Image.open(io.BytesIO(data)).convert("RGB")
    except Exception as e:
        return False, f"not decodable: {e}"
    w, h = img.size
    if w < MIN_IMAGE_W or h < MIN_IMAGE_H:
        return False, f"too small ({w}x{h})"
    if not generated:
        arr = np.array(img, dtype=np.float32)
        variance = float(np.var(arr))
        if variance < MIN_VARIANCE:
            return False, f"blank/solid (variance={variance:.1f})"
    return True, "ok"

