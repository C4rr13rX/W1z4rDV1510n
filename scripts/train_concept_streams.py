# Scene composition trainer
# ---------------------------------------------------------------------------

def train_scene_pair(c_left: Concept, c_right: Concept,
                      img_left: bytes, img_right: bytes,
                      client: httpx.Client) -> bool:
    scene_bytes, spans = compose_scene(img_left, img_right, c_left.word, c_right.word)
    ok, reason = validate_image(scene_bytes, f"{c_left.word}+{c_right.word}", generated=True)
    if not ok:
        print(f"  [SKIP scene] {c_left.word}+{c_right.word}: {reason}")
        return False
    combined_text = f"{c_left.word} and {c_right.word}. {c_left.definition} {c_right.definition}"
    payload = {
        "modality": "page",
        "data_b64": b64(scene_bytes),
        "text": combined_text,
        "spans": spans,
        "keys": keyboard_type(f"{c_left.word} {c_right.word}"),
        "lr_scale": LR_SCENE,
    }
    return api_train(client, payload, f"scene:{c_left.word}+{c_right.word}")

