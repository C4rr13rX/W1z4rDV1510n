# Cross-modal probe
# ---------------------------------------------------------------------------

def probe_cross_modal(concept: Concept, img_bytes: bytes, client: httpx.Client) -> dict:
    """
    Test whether image zone labels activate the correct text:word_ label,
    and whether the text:word_ label activates image zone labels.
    """
    word = concept.word
    results = {"word": word, "img_to_text": None, "text_to_img": None}

    # Extract image labels by encoding the image (request the node to encode it)
    # We probe by seeding with the word label and looking for image labels
    activated_from_text = api_propagate(client, [f"txt:word_{word}"], hops=3, top_k=15)
    img_labels = [a for a in activated_from_text if a["label"].startswith("img:")]
    txt_labels_from_text = [a for a in activated_from_text if "word_" in a["label"]]

    results["text_to_img"] = {
        "img_labels_activated": len(img_labels),
        "top_img": img_labels[:3],
        "related_words": [a["label"].replace("txt:word_", "") for a in txt_labels_from_text[:5]],
    }

    # Check if image-zone seeds activate the word (needs image encoded as labels)
    # Use hue labels from the concept's color if available
    if concept.color_rgb:
        # For color concepts: seed with the expected hue label
        import colorsys
        r, g, b = [x/255 for x in concept.color_rgb]
        h, s, v = colorsys.rgb_to_hsv(r, g, b)
        hue_bin = int(h * 16) % 16
        seeds = [f"img:h{hue_bin}"]
        activated_from_img = api_propagate(client, seeds, hops=3, top_k=15)
        word_labels = [a for a in activated_from_img if "word_" in a["label"]]
        results["img_to_text"] = {
            "seed": seeds,
            "word_labels_activated": len(word_labels),
            "top_words": [a["label"].replace("txt:word_", "") for a in word_labels[:5]],
        }
    return results

