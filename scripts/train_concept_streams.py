def generate_text_card_image(concept: Concept) -> bytes:
    """
    Universal fallback: a clean text card with the word large, definition below,
    and a colored top/bottom stripe keyed to concept type.
    Used when no photo or domain-specific synthetic image is available.
    Semantically correct by construction -- the word and definition are written
    directly on the image.
    """
    TYPE_STRIPE = {
        "descriptor": (100, 149, 237),   # cornflower blue
        "abstract":   (147, 112, 219),   # medium purple
        "action":     ( 60, 179, 113),   # medium sea green
        "concrete":   (205, 133,  63),   # peru brown
    }
    stripe = TYPE_STRIPE.get(concept.type, (120, 120, 120))
    W, H = 480, 320
    img = Image.new("RGB", (W, H), color=(252, 251, 248))
    draw = ImageDraw.Draw(img)

    draw.rectangle([0, 0, W, 14], fill=stripe)
    draw.rectangle([0, H - 14, W, H], fill=stripe)

    try:
        font_word = ImageFont.truetype("C:/Windows/Fonts/arialbd.ttf", 72)
        font_def  = ImageFont.truetype("C:/Windows/Fonts/arial.ttf", 18)
    except Exception:
        font_word = ImageFont.load_default()
        font_def  = ImageFont.load_default()

    draw.text((W // 2, 80), concept.word.upper(),
              fill=(30, 30, 30), font=font_word, anchor="mm")

    # Word-wrap definition to ~50 chars per line
    words = concept.definition.split()
    lines: list[str] = []
    cur: list[str] = []
    for w in words:
        if len(" ".join(cur + [w])) > 52 and cur:
            lines.append(" ".join(cur))
            cur = [w]
        else:
            cur.append(w)
    if cur:
        lines.append(" ".join(cur))

    y = 160
    for line in lines[:4]:
        draw.text((W // 2, y), line, fill=(70, 70, 70), font=font_def, anchor="mm")
        y += 28

    buf = io.BytesIO()
    img.save(buf, format="JPEG", quality=90)
    return buf.getvalue()

