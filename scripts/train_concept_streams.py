def generate_color_image(concept: Concept) -> bytes:
    rgb = concept.color_rgb or (128, 128, 128)
    img = Image.new("RGB", (400, 300), color=rgb)
    draw = ImageDraw.Draw(img)
    # White label bar at bottom
    draw.rectangle([0, 250, 400, 300], fill=(255, 255, 255))
    draw.text((200, 275), concept.word.upper(), fill=(0, 0, 0), anchor="mm")
    # Add color swatch gradient strip
    for x in range(400):
        t = x / 400
        r = int(rgb[0] * (0.5 + 0.5 * t))
        g = int(rgb[1] * (0.5 + 0.5 * t))
        b = int(rgb[2] * (0.5 + 0.5 * t))
        draw.line([(x, 0), (x, 245)], fill=(r, g, b))
    buf = io.BytesIO()
    img.save(buf, format="JPEG", quality=90)
    return buf.getvalue()

def generate_shape_image(concept: Concept) -> bytes:
    img = Image.new("RGB", (400, 400), color=(245, 245, 245))
    draw = ImageDraw.Draw(img)
    cx, cy = 200, 200
    fill = (70, 130, 180)
    outline = (20, 60, 100)
    word = concept.word
    if word == "circle":
        draw.ellipse([cx-150, cy-150, cx+150, cy+150], fill=fill, outline=outline, width=4)
    elif word == "square":
        draw.rectangle([cx-130, cy-130, cx+130, cy+130], fill=fill, outline=outline, width=4)
    elif word == "triangle":
        draw.polygon([(cx, cy-150), (cx-140, cy+130), (cx+140, cy+130)],
                     fill=fill, outline=outline)
    elif word == "rectangle":
        draw.rectangle([cx-180, cy-100, cx+180, cy+100], fill=fill, outline=outline, width=4)
    elif word == "sphere":
        # Sphere illusion with shading
        draw.ellipse([cx-150, cy-150, cx+150, cy+150], fill=(100, 160, 220), outline=outline, width=3)
        draw.ellipse([cx-80, cy-90, cx+10, cy-20], fill=(200, 230, 255))
    # Label
    draw.text((200, 380), word.upper(), fill=(50, 50, 50), anchor="mm")
    buf = io.BytesIO()
    img.save(buf, format="JPEG", quality=92)
    return buf.getvalue()

