def train_concept(concept: Concept, images: list[bytes], audio: Optional[bytes],
                   client: httpx.Client, verbose: bool = False) -> dict:
    """
    Fire all modalities for one concept simultaneously, in the correct order:
      1. Anchor frame  -- image + label text + keyboard typing
      2. Definition    -- image + full definition + keyboard typing
      3. Audio frame   -- image + audio + text (if audio available)
      4. QA ingest     -- Hebbian Q->A pair with confidence 0.95
      5. Episode       -- record context->definition episode
    Returns stats dict.
    """
    word = concept.word
    defn = concept.definition
    stats = {"word": word, "frames": 0, "qa": 0, "audio": bool(audio)}

    if not images:
        print(f"  [SKIP] {word}: no valid images")
        return stats

    img0 = images[0]
    img1 = images[1] if len(images) > 1 else img0

    # -- 1. Anchor frame: image + bold label + keyboard typing ----------------
    spans_anchor = [{
        "text": word, "role": "label", "bold": True, "italic": False,
        "size_ratio": 2.0, "indent": 0,
        "x_frac": 0.5, "y_frac": 0.88,
        "seq_index": 0, "seq_total": 1,
    }]
    payload_anchor = {
        "modality": "page",
        "data_b64": b64(img0),
        "text": word,
        "spans": spans_anchor,
        "keys": keyboard_type(word),
        "lr_scale": LR_ANCHOR,
    }
    if api_train(client, payload_anchor, f"{word}:anchor"):
        stats["frames"] += 1

    # -- 2. Definition frame: different image + full definition + keyboard ----
    # Build structured text spans from definition sentences
    sentences = [s.strip() for s in defn.replace(".", ".|||").split("|||") if s.strip()]
    spans_def = []
    for i, sent in enumerate(sentences):
        spans_def.append({
            "text": sent, "role": "body", "bold": False, "italic": False,
            "size_ratio": 1.0, "indent": 0,
            "x_frac": 0.5, "y_frac": 0.2 + 0.15 * i,
            "seq_index": i, "seq_total": len(sentences),
        })
    # Add concept word as heading span
    spans_def.insert(0, {
        "text": word, "role": "heading", "bold": True, "italic": False,
        "size_ratio": 1.8, "indent": 0,
        "x_frac": 0.5, "y_frac": 0.05,
        "seq_index": 0, "seq_total": len(sentences) + 1,
    })

    payload_defn = {
        "modality": "page",
        "data_b64": b64(img1),
        "text": defn,
        "spans": spans_def,
        "keys": keyboard_type(defn[:80]),
        "lr_scale": LR_CONTEXT,
    }
    if api_train(client, payload_defn, f"{word}:defn"):
        stats["frames"] += 1

    # -- 3. Audio frame (if Polly generated it) --------------------------------
    if audio:
        payload_audio = {
            "modality": "full",
            "data_b64": b64(img0),
            "text": f"{word}. {concept.example}",
            "spans": spans_anchor,
            "lr_scale": LR_CONTEXT,
        }
        # Note: the audio modality endpoint expects audio data under a separate
        # field. We embed it as the data_b64 with modality="audio" in a separate call.
        payload_audio_only = {
            "modality": "audio",
            "data_b64": b64(audio),
            "text": word,
            "spans": spans_anchor,
            "lr_scale": LR_CONTEXT,
        }
        if api_train(client, payload_audio_only, f"{word}:audio"):
            stats["frames"] += 1

    # -- 4. Q&A ingest ---------------------------------------------------------
    qa_id = hashlib.sha256(f"streams|{word}|{defn}".encode()).hexdigest()[:16]
    ingested = api_ingest_qa(client, [{
        "qa_id": qa_id,
        "question": f"What is {word}?" if concept.type not in ("action", "descriptor") else f"What does {word} mean?",
        "answer": defn,
        "book_id": "concept_streams_v1",
        "page_index": 0,
        "confidence": 0.97,
        "evidence": defn,
        "review_status": "VERIFIED",
    }])
    stats["qa"] = ingested

    # -- 5. Episode recording (context label -> definition) --------------------
    api_record_episode(
        client,
        context_labels=[f"txt:word_{word}"],
        predicted=defn,
        actual=defn,
        surprise=0.0,
    )

    if verbose:
        print(f"  {word}: {stats['frames']} frames, qa={stats['qa']}, audio={stats['audio']}")
    return stats

# ---------------------------------------------------------------------------
# Temporal lifecycle trainer
# ---------------------------------------------------------------------------

def train_lifecycle_sequence(seq: list[str], client: httpx.Client,
                              all_images: dict[str, bytes]) -> bool:
    """Train a temporal concept chain as a train_sequence call.
    Frames are text-only if no image, image+text if available."""
    frames = []
    t = 0.0
    for word in seq:
        img = all_images.get(word)
        # No keyboard events here: lifecycle frames are conceptual-chain signals,
        # not typing simulations.  Keyboard events inflate the label count per frame
        # which makes the Hebbian bridge sets large and slows motif observation.
        frame = {
            "t_secs": round(t, 2),
            "modality": "page" if img else "text",
            "text": word,
            "spans": [{
                "text": word, "role": "label", "bold": True, "italic": False,
                "size_ratio": 1.5, "indent": 0,
                "x_frac": 0.5, "y_frac": 0.5,
                "seq_index": 0, "seq_total": 1,
            }],
            "lr_scale": LR_SEQ,
        }
        if img:
            frame["data_b64"] = b64(img)
        frames.append(frame)
        t += 2.0
    return api_train_sequence(client, frames, tau=TEMP_TAU)

