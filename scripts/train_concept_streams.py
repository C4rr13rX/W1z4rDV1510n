def pcm_to_wav(pcm_bytes: bytes, sample_rate: int = 16000) -> bytes:
    data_size = len(pcm_bytes)
    header = struct.pack(
        "<4sI4s4sIHHIIHH4sI",
        b"RIFF", 36 + data_size, b"WAVE",
        b"fmt ", 16, 1, 1,       # PCM, mono
        sample_rate,
        sample_rate * 2,         # byte rate: sr * channels * bits/8
        2, 16,                   # block align, bits per sample
        b"data", data_size,
    )
    return header + pcm_bytes

def generate_audio(text: str, voice_id: str = "Matthew") -> Optional[bytes]:
    """Generate WAV audio via AWS Polly.  Returns None if unavailable."""
    if not _HAS_BOTO3:
        return None
    try:
        session = boto3.Session(profile_name=AWS_PROFILE, region_name=AWS_REGION)
        polly = session.client("polly")
        resp = polly.synthesize_speech(
            Text=text[:500],
            Engine="neural",
            VoiceId=voice_id,
            OutputFormat="pcm",
            SampleRate="16000",
        )
        pcm = resp["AudioStream"].read()
        return pcm_to_wav(pcm)
    except Exception as e:
        print(f"  [WARN] Polly failed for '{text[:30]}...': {e}")
        return None

# ---------------------------------------------------------------------------
# Keyboard typing helper
# ---------------------------------------------------------------------------

def keyboard_type(text: str, start_t: float = 0.0, delay: float = 0.11) -> list:
    """Simulate typing text character by character with realistic timing.
    Ends with a space so KeyboardBitsEncoder completes the word and emits
    the txt:word_* label that cross-activates with the image modality."""
    events = []
    t = start_t
    for ch in text:
        upper = ch.isupper()
        key = ch.lower()
        events.append({"key": key, "ctrl": False, "shift": upper, "alt": False, "t_secs": round(t, 3)})
        t += delay + (0.05 if ch == " " else 0.0)
    # Trailing space completes the last word
    if text and not text.endswith(" "):
        events.append({"key": " ", "ctrl": False, "shift": False, "alt": False, "t_secs": round(t, 3)})
    return events

# ---------------------------------------------------------------------------
# Scene compositor -- two concepts side by side with correct spatial labels
# ---------------------------------------------------------------------------

def compose_scene(img_left: bytes, img_right: bytes,
                  label_left: str, label_right: str) -> tuple[bytes, list]:
    """
    Place two concept images side by side on a white canvas.
    Returns (scene_jpeg_bytes, text_spans_positioned_at_object_centers).

    The spans have x_frac matching where the object ACTUALLY IS in the scene,
    so the txt:zone_* labels co-activate with the img:z*_* labels for that zone.
    """
    SZ = 280  # size for each object panel
    MARGIN = 20
    W = SZ * 2 + MARGIN * 3
    H = SZ + MARGIN * 2 + 40  # +40 for label row at bottom

    img_l = Image.open(io.BytesIO(img_left)).convert("RGB").resize((SZ, SZ))
    img_r = Image.open(io.BytesIO(img_right)).convert("RGB").resize((SZ, SZ))
    canvas = Image.new("RGB", (W, H), (255, 255, 255))
    canvas.paste(img_l, (MARGIN, MARGIN))
    canvas.paste(img_r, (SZ + MARGIN * 2, MARGIN))

    draw = ImageDraw.Draw(canvas)
    cx_l = (MARGIN + SZ // 2) / W
    cx_r = (SZ + MARGIN * 2 + SZ // 2) / W
    draw.text((int(cx_l * W), H - 20), label_left.upper(),  fill=(50, 50, 50), anchor="mm")
    draw.text((int(cx_r * W), H - 20), label_right.upper(), fill=(50, 50, 50), anchor="mm")

    buf = io.BytesIO()
    canvas.save(buf, format="JPEG", quality=88)

    spans = [
        {"text": label_left,  "role": "label", "bold": True, "italic": False,
         "size_ratio": 1.5, "indent": 0,
         "x_frac": round(cx_l, 3), "y_frac": 0.5,
         "seq_index": 0, "seq_total": 2},
        {"text": label_right, "role": "label", "bold": True, "italic": False,
         "size_ratio": 1.5, "indent": 0,
         "x_frac": round(cx_r, 3), "y_frac": 0.5,
         "seq_index": 1, "seq_total": 2},
    ]
    return buf.getvalue(), spans

